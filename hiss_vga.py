def unhiss_palette(hiss):
    """Decipher a standard VGA palette from weasel hisses"""
    dec = hiss.lower().replace("\n", " ").split(" ")

    out = []
    pr = []
    while dec:
        nxt = dec.pop(0)
        l = len(nxt)
        if l == 4:
            out.append(0)
            continue
        elif l == 1:
            pr.append(5)
        elif l > 4:
            pr.append(9-l)
        else:
            pxt = dec.pop(0)
            k = len(pxt) - 1
            pr.append((6 if l == 2 else 11) + k)
        if len(pr) == 2:
            out.append(int(f"{hex(pr[0])[2:]}{hex(pr[1])[2:]}", 16))
            pr = []
    return out

